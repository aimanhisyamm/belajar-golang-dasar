package main

import "fmt"

func main() {
	var name string

	name = "Aiman Hisyam"
	fmt.Println(name)

	name = "Muhamad Aiman"
	fmt.Println(name)

	var friendName = "Arya"
	fmt.Println(friendName)

	var age = 24
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Muhamad Aiman"
		lastName  = "Hisyam"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)
}
